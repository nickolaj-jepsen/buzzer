/**
 * Generates a random username by combining a random adjective and noun.
 *
 * @returns A random username string.
 */
export const randomUsername = (): string => {
  const randomAdjective =
    adjectives[Math.floor(Math.random() * adjectives.length)];
  const randomNoun = nouns[Math.floor(Math.random() * nouns.length)];

  return `${randomAdjective}${randomNoun}`;
};

/**
 * Generates an easily shareable random room code.
 *
 * @returns A random string of 8 characters suitable for a room ID.
 */
export const randomRoomId = (): string => {
  return Math.random().toString(36).substring(2, 10);
};
